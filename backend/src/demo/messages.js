export function interpolateCallMessage(message, { businessName, companyName } = {}) {
  const business = businessName || "your business"
  const company = companyName || businessName || "your company"
  // The supplied source copy uses localized [Business]/[Company] tokens;
  // accept those as well as the documented tenant placeholders.
  return message
    .replaceAll("{business_name}", business)
    .replaceAll("{company_name}", company)
    .replace(/\[(?:Business|business|Company|Negocio|Empresa|व्यवसाय|कंपनी|వ్యాపారం|కంపెనీ|வணிகம்|நிறுவனம்|ವ್ಯಾಪಾರ|ಕಂಪನಿ|ബിസിനസ്സ്|कंपनी|બિઝનેસ|કંપની|ব্যবসা|কোম্পানি|ਬਿਜ਼ਨਸ|ਕੰਪਨੀ|ব্যৱসায়|কোম্পানী|کاروبার|کمپنی)\]/gu, token => token.includes("Company") || token.includes("Empresa") || token.includes("कंपनी") || token.includes("కంపెనీ") || token.includes("நிறுவனம்") || token.includes("ಕಂಪನಿ") || token.includes("കമ്പനി") || token.includes("કંપની") || token.includes("কোম্পানি") || token.includes("ਕੰਪਨੀ") || token.includes("কোম্পানী") || token.includes("کمپنی") ? company : business)
}

export function getCallMessages({ useCase, language, businessName, companyName }) {
  const messages = CALL_MESSAGES[useCase]?.[language] || CALL_MESSAGES[useCase]?.en || CALL_MESSAGES.order_taking.en
  return { greeting:interpolateCallMessage(messages.greeting, { businessName, companyName }), ending:interpolateCallMessage(messages.ending, { businessName, companyName }) }
}
